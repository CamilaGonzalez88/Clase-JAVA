public class Operadores {
    public static void main(String[] args){
       // Aritmeticos
        System.out.println(3+2);
        System.out.println(3-2);
        System.out.println(3*2);
        System.out.println(3/2);
        System.out.println(3%2);

     // Operadores de asignación
        int num1 = 35;
        num1 += 3; // num1 = num1 +3
        num1 *= 4; // num1 = num1 *4
        num1 /= 2; // num1 = num1 /2
        num1 %= 2; // num1 = num1 %2
        System.out.println(num1);














    }
}
